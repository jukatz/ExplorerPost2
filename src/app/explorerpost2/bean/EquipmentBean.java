package app.explorerpost2.bean;

public class EquipmentBean {
	private Integer serial = null;
	private String equipId = null;
	private String equipType = null;
	private String brand = null;
	private String model = null;
	private Integer status = null;
	private Integer userId = null;
	private String audtUserId = null;
	
	public EquipmentBean(){}

	public Integer getSerial() {
		return serial;
	}

	public void setSerial(Integer serial) {
		this.serial = serial;
	}

	public String getEquipId() {
		return equipId;
	}

	public void setEquipId(String equipId) {
		this.equipId = equipId;
	}

	public String getEquipType() {
		return equipType;
	}

	public void setEquipType(String equipType) {
		this.equipType = equipType;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getAudtUserId() {
		return audtUserId;
	}

	public void setAudtUserId(String audtUserId) {
		this.audtUserId = audtUserId;
	}
	
	
	
	
}
